# datasets.sh — Dataset definitions and baseline-generated index paths.
# Sourced by all run scripts. Caller must set repo_root before sourcing.

source "$(dirname "${BASH_SOURCE[0]}")/config.sh"

# ---------------------------------------------------------------------------
# ANN base graphs (raw dataset files under anndataset_root)
# Format per entry: "tag|path:format|type|dist|max_points"
#   tag        — short identifier used for output dirs/files
#   path:fmt   — ANNlib dataset spec (absolute path + format token)
#   type       — uint8 | int8 | float
#   dist       — L2 | angular
#   max_points — number of base vectors to load
# ---------------------------------------------------------------------------
  # "bigann1M|${anndataset_root}/sift/bigann.base.1B.u8bin:u8bin|uint8|L2|1000000"
  # "deep1M_yandex|${anndataset_root}/yandex_deep/learn.350M.fbin:fbin|float|L2|1000000"
DATASETS=(
  "bigann1M|${anndataset_root}/BIGANN/base.1B.u8bin:u8bin|uint8|L2|1000000"
  "deep1M_yandex|${anndataset_root}/Yandex-DEEP/base.1B.fbin:fbin|float|L2|1000000"
  "openai1M|${anndataset_root}/openai/openai_large_5m/base.fbin:fbin|float|angular|1000000"
  "cohere1M|${anndataset_root}/cohere/cohere_large_10m/base.fbin:fbin|float|angular|1000000"
)

# ---------------------------------------------------------------------------
# Optional: cross product with synth-timestamp distributions.
# When DISTRIBUTIONS_STR is set (e.g. by run_all_baselines.sh), each base
# dataset is expanded to a virtual entry per distribution with
# tag = <base_tag>__<distribution>. All downstream paths (sorted_root,
# cache_root, result_root) become distribution-aware automatically because
# every existing wrapper keys on ${tag}.
# Unset => original 4 entries (legacy behavior preserved).
# ---------------------------------------------------------------------------
if [[ -n "${DISTRIBUTIONS_STR:-}" ]]; then
  read -ra _distributions <<< "${DISTRIBUTIONS_STR}"
  _base_datasets=("${DATASETS[@]}")
  DATASETS=()
  for _entry in "${_base_datasets[@]}"; do
    IFS='|' read -r _tag _path _type _dist _max <<< "${_entry}"
    [[ -n "${_tag}" ]] || continue
    for _distribution in "${_distributions[@]}"; do
      DATASETS+=("${_tag}__${_distribution}|${_path}|${_type}|${_dist}|${_max}")
    done
  done
  unset _distributions _base_datasets _entry _tag _path _type _dist _max _distribution
fi

